// 工业级标准译码器模块：支持ALU/立即数指令，预留扩展接口
// 指令格式约定（扩展友好型）：
// code[31]    ：指令有效位（1=无效，0=有效）
// code[30:29] ：指令类型（optype）
//               00=ALU运算指令 | 01=立即数写入指令 
//               10=跳转指令（预留） | 11=访存指令（预留）
// code[28:27] ：操作子类型（opcode）- 不同指令类型下含义不同
//               ALU指令：00=add | 01=sub | 10=mul | 11=div
//               其他指令：预留
// code[26:25] ：写寄存器地址（waddr）- 所有写指令共用
// code[24:23] ：读寄存器地址1（raddr1）- ALU/访存指令用
// code[22:21] ：读寄存器地址2（raddr2）- ALU指令用
// code[20:0]   ：指令参数（立即数/跳转地址/访存地址等）
module decoder(
    input clk,          // 保留你原有的clk接口（仅占位，内部无时序逻辑）
    input rst_n,
    input [31:0] code,
    output reg alu_en,       // ALU运算指令使能
    output reg [1:0] opcode, // 运算类型（仅ALU指令有效）
    // 寄存器读地址（仅ALU指令有效）
    output reg [1:0] raddr1,
    output reg [1:0] raddr2,
    // 写寄存器地址（ALU/立即数指令共用）
    output reg [1:0] waddr,
    output reg we,           // 通用写使能
    // 新增：立即数指令相关信号（保留你原有的命名）
    output reg imm_en,       // 立即数写入指令使能
    output reg [31:0] imm_data // 立即数数据
);

// 本地参数：仅内部使用，不修改外部接口
localparam OPTYPE_ALU    = 2'b00;  // ALU指令（code[30:29]）
localparam OPTYPE_IMM    = 2'b01;  // 立即数指令（code[30:29]）

// 核心组合逻辑：工业级标准写法（先赋默认值→分层分支）
// 注：保留clk入参但不接入敏感列表，完全兼容你原有接口
always @(*) begin
    // ==============================================
    // 步骤1：赋默认值（杜绝锁存器，所有信号先清零）
    // ==============================================
    alu_en    = 1'b0;
    opcode    = 2'b00;
    raddr1    = 2'd0;
    raddr2    = 2'd0;
    waddr     = 2'd0;
    we        = 1'b0;
    imm_en    = 1'b0;
    imm_data  = 32'd0;

    // ==============================================
    // 步骤2：复位判断（复位时保持默认值）
    // ==============================================
    if (rst_n) begin
        // ==============================================
        // 步骤3：指令有效性判断（code[31]=0有效）
        // ==============================================
        if (!code[31]) begin
            // 通用写信号：所有有效指令都赋值
            waddr  = code[26:25];
            we     = 1'b1;

            // ==============================================
            // 步骤4：指令类型分支解析
            // ==============================================
            case (code[30:29])
                // 分支1：ALU运算指令
                OPTYPE_ALU: begin
                    alu_en   = 1'b1;
                    opcode   = code[28:27];
                    raddr1   = code[24:23];
                    raddr2   = code[22:21];
                end

                // 分支2：立即数写入指令
                OPTYPE_IMM: begin
                    imm_en   = 1'b1;
                    imm_data = {7'd0, code[24:0]};
                end

                // 默认分支：无效指令类型（保持默认值）
                default: ;
            endcase
        end
    end
end

endmodule
